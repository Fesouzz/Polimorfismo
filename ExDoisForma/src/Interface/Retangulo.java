package Interface;

/**
 *
 * @author fesil
 */
public class Retangulo implements Forma{
    private double altura;
    private double largura;

    public Retangulo(double altura, double largura) {
        this.altura = altura;
        this.largura = largura;
    }
    
    @Override
    public double calcularArea(){
    return altura*largura;
    }
}
